
public class Coffee {

    // propeties
    private String name;
    private double weight;
    private double price;

    // methods
    // constructor
    public Coffee() {
        this.name = "";
        this.weight = 0;
        this.price = 0;
    }

    public Coffee(String n, double w, double p) {
        this.name = n;
        this.weight = w;
        this.price = p;
    }
}
